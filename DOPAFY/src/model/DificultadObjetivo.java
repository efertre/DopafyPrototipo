package model;

/**
 * Enumeración que representa las diferentes dificultades para los objetivos.
 */
public enum DificultadObjetivo {

    /**
     * Nivel de dificultad sencillo.
     */
    SENCILLO,

    /**
     * Nivel de dificultad moderado.
     */
    MODERADO,

    /**
     * Nivel de dificultad difícil.
     */
    DIFICIL,

    /**
     * Nivel de dificultad retador.
     */
    RETADOR,

    /**
     * Nivel de dificultad desafiante.
     */
    DESAFIANTE
}
